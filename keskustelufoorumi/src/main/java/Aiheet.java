public class Aiheet {
    private String nimi;
    private String kuvaus;
    private int alueid;

    public Aiheet() {
        this.nimi = nimi;
        this.kuvaus = kuvaus;
        this.alueid = alueid;
    }

    public String getNimi() {
        return nimi;
    }

    public void setNimi(String nimi) {
        this.nimi = nimi;
    }

    public String getKuvaus() {
        return kuvaus;
    }

    public void setKuvaus(String kuvaus) {
        this.kuvaus = kuvaus;
    }

    public int getAlueid() {
        return alueid;
    }

    public void setAlueid(int alueid) {
        this.alueid = alueid;
    }

}
